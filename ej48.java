/*48) Caracteres de escape y formato
Crea un programa que imprima un mensaje en varias líneas con tabulaciones y saltos de línea, por ejemplo:
Nombre: Juan
Edad: 20
Ciudad: Resistencia
*/

public class CaracteresEscape {
    public static void main(String[] args) {
        // Mostrar información con formato usando caracteres de escape
        // \n para salto de línea y \t para tabulación
        System.out.println("Nombre:\tJuan");
        System.out.println("Edad:\t20");
        System.out.println("Ciudad:\tResistencia");
    }
}
